public class Circumfrence {
    public static void main(String[] args) {
        int radius = 85;
        int piInt = 3;
        double piDouble = 3.142857;
        float piFloat = 3.142857142857143f;
        System.out.println("The Radius Is in Intger Value : "+2*radius*piInt);
        System.out.println("The Radius Is in Intger Value : "+2*radius*piFloat);
        System.out.println("The Radius Is in Intger Value : "+2*radius*piDouble);

    }
}
