/*
* Default Value of DataMember
*/
class VarDemo3 {
	static int i;
	static double d;
	static char c;
	static boolean b;
	static String s;
	public static void main(String[] args) {
		System.out.println("Default Value Of Integer is "+i);
		System.out.println("Default Value Of Double is "+d);
		System.out.println("Default Value Of Character is "+c);
		System.out.println("Default Value Of Boolean is "+b);
		System.out.println("Default Value Of String is "+s);
	}
}